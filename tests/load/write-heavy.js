/**
 * Write-Heavy Load Test
 *
 * Simulates heavy CRUD operations to test database write performance,
 * connection pool behavior, and transaction handling under load.
 *
 * Run: k6 run tests/load/write-heavy.js
 * Run with custom URL: BASE_URL=http://staging.nucrm.com k6 run tests/load/write-heavy.js
 */

import http from 'k6/http';
import { check, sleep } from 'k6';
import { Rate, Trend, Counter } from 'k6/metrics';

const errorRate = new Rate('errors');
const writeLatency = new Trend('write_latency');
const readLatency = new Trend('read_latency');
const writesTotal = new Counter('writes_total');
const readsTotal = new Counter('reads_total');

export const options = {
  scenarios: {
    write_heavy: {
      executor: 'ramping-vus',
      startVUs: 0,
      stages: [
        { duration: '30s', target: 5 },
        { duration: '1m', target: 20 },
        { duration: '30s', target: 0 },
      ],
      gracefulRampDown: '10s',
    },
  },
  thresholds: {
    http_req_duration: ['p(95)<1000'],
    errors: ['rate<0.05'],
    write_latency: ['p(95)<500'],
  },
};

const BASE_URL = __ENV.BASE_URL || 'http://localhost:3000';
const EMAIL = __ENV.EMAIL || 'superadmin@nucrm.com';
const PASSWORD = __ENV.PASSWORD || 'admin123';

export function setup() {
  const loginRes = http.post(`${BASE_URL}/api/auth/login`,
    JSON.stringify({ email: EMAIL, password: PASSWORD }),
    { headers: { 'Content-Type': 'application/json' } }
  );

  check(loginRes, { 'setup: login successful': (r) => r.status === 200 });

  const token = loginRes.json('token');
  return { token };
}

export default function(data) {
  const headers = {
    'Content-Type': 'application/json',
    'Authorization': `Bearer ${data.token}`,
  };

  // WRITE: Create contact
  const createContact = http.post(`${BASE_URL}/api/tenant/contacts`,
    JSON.stringify({
      first_name: `LoadTest_${Date.now()}`,
      last_name: 'User',
      email: `loadtest_${Date.now()}@example.com`,
      phone: '+1234567890',
      job_title: 'Test Role',
    }),
    { headers }
  );
  check(createContact, { 'create contact 201': (r) => r.status === 201 }) || errorRate.add(1);
  writeLatency.add(createContact.timings.duration);
  writesTotal.add(1);

  const contactId = createContact.json('data.id');

  if (contactId) {
    // READ: Get contact
    const getContact = http.get(`${BASE_URL}/api/tenant/contacts/${contactId}`, { headers });
    check(getContact, { 'get contact 200': (r) => r.status === 200 }) || errorRate.add(1);
    readLatency.add(getContact.timings.duration);
    readsTotal.add(1);

    // WRITE: Update contact
    const updateContact = http.put(`${BASE_URL}/api/tenant/contacts/${contactId}`,
      JSON.stringify({ job_title: 'Updated Role' }),
      { headers }
    );
    check(updateContact, { 'update contact 200': (r) => r.status === 200 }) || errorRate.add(1);
    writeLatency.add(updateContact.timings.duration);
    writesTotal.add(1);

    // WRITE: Create task for contact
    const createTask = http.post(`${BASE_URL}/api/tenant/tasks`,
      JSON.stringify({
        title: `Load Test Task ${Date.now()}`,
        description: 'Auto-generated by load test',
        priority: 'medium',
        contact_id: contactId,
      }),
      { headers }
    );
    check(createTask, { 'create task 201': (r) => r.status === 201 }) || errorRate.add(1);
    writeLatency.add(createTask.timings.duration);
    writesTotal.add(1);

    // WRITE: Delete task
    const taskId = createTask.json('data.id');
    if (taskId) {
      const deleteTask = http.del(`${BASE_URL}/api/tenant/tasks/${taskId}`, null, { headers });
      check(deleteTask, { 'delete task 200': (r) => r.status === 200 }) || errorRate.add(1);
      writeLatency.add(deleteTask.timings.duration);
      writesTotal.add(1);
    }

    // WRITE: Delete contact
    const deleteContact = http.del(`${BASE_URL}/api/tenant/contacts/${contactId}`, null, { headers });
    check(deleteContact, { 'delete contact 200': (r) => r.status === 200 }) || errorRate.add(1);
    writeLatency.add(deleteContact.timings.duration);
    writesTotal.add(1);
  }

  // READ: List contacts (paginated)
  const listContacts = http.get(`${BASE_URL}/api/tenant/contacts?limit=20&offset=0`, { headers });
  check(listContacts, { 'list contacts 200': (r) => r.status === 200 }) || errorRate.add(1);
  readLatency.add(listContacts.timings.duration);
  readsTotal.add(1);

  sleep(0.5);
}

export function handleSummary(data) {
  return {
    'stdout': textSummary(data, 'Write-Heavy Load Test'),
    'tests/load/write-heavy-summary.json': JSON.stringify(data, null, 2),
  };
}

function textSummary(data, title) {
  const metrics = data.metrics;
  return `
=== ${title} ===

Duration: ${new Date(data.state.testRunDurationMs).toISOString().substr(11, 8)}
Total Requests: ${metrics.http_reqs.values.count}
Error Rate: ${(metrics.errors.values.rate * 100).toFixed(2)}%

Write Latency (p95): ${metrics.write_latency.values['p(95)']?.toFixed(0) || 'N/A'}ms
Read Latency (p95): ${metrics.read_latency.values['p(95)']?.toFixed(0) || 'N/A'}ms

Total Writes: ${metrics.writes_total.values.count}
Total Reads: ${metrics.reads_total.values.count}

HTTP p95: ${metrics.http_req_duration.values['p(95)']?.toFixed(0) || 'N/A'}ms
HTTP p99: ${metrics.http_req_duration.values['p(99)']?.toFixed(0) || 'N/A'}ms
`;
}
